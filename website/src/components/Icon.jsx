export function Icon({ name, size = 18, color, className = '', style = {} }) {
  const Cmp = ICONS[name] || Circle;
  return (
    <span
      className={className}
      style={{
        display: 'inline-flex',
        alignItems: 'center',
        justifyContent: 'center',
        width: size,
        height: size,
        color: color || 'currentColor',
        ...style,
      }}
    >
      <Cmp size={size} strokeWidth={1.5} />
    </span>
  );
}
