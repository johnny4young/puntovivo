// Dynamic lucide icon resolver for the secondary pages.
//
// The design prototypes used a Babel-in-browser `<Icon name="scan-line" />`
// helper backed by the lucide UMD build (kebab-case names). Here we keep that
// kebab-case `name` API but resolve it through an explicit named-import map so
// Vite tree-shakes lucide-react down to only the icons the pages use — a
// `import * as Lucide` barrel would otherwise pull all ~5000 icons into the
// shared chunk. Add a new entry to ICONS when a page references a new name.
